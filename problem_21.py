def switch_it_up(number):
    num_str = ""
    if number == 1:
        num_str += "One"
    elif number == 2:
        num_str += "Two"
    elif number == 3:
        num_str += "Three"
    elif number == 4:
        num_str += "Four"
    elif number == 5:
        num_str += "Five"
    elif number == 6:
        num_str += "Six"
    elif number == 7:
        num_str += "Seven"
    elif number == 8:
        num_str += "Eight"
    elif number == 9:
        num_str += "Nine"
    elif number == 10:
        num_str += "Ten"
    elif number == 0:
        num_str += "Zero"
    
    return num_str